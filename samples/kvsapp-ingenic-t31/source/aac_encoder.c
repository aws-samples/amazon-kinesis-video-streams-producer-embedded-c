int AacEncoder_encode(void *pHandle, uint8_t *pPcmBuf, size_t uPcmBufLen, uint8_t *pEncBuf, size_t uEncBufSize, size_t *puEncBufLen, size_t *puPcmBufUsed, uint64_t *puTimestampMs)
{
    int res = ERRNO_NONE;
    AacEncoder_t *pAacEncoder = (AacEncoder_t *)pHandle;
    size_t uEncBufLen = 0;
    const uint8_t *pSilentAacBuf = NULL;
    size_t uSilentAacLen = 0;

    if (pAacEncoder == NULL || pPcmBuf == NULL || uPcmBufLen == 0 || puEncBufLen == NULL || puPcmBufUsed == NULL)
    {
        res = ERRNO_FAIL;
    }
    else
    {
        if (pEncBuf == NULL)
        {
            /* If the output buffer size is null, we calculated the needed output length without really encoding it. */
            if (uPcmBufLen + pAacEncoder->uPcmBufLen >= pAacEncoder->uPcmBufSize)
            {
                if (pAacEncoder->uPcmBufLen == 0)
                {
                    pAacEncoder->uPcmTimestampMs = getEpochTimestampInMs();
                }
                *puEncBufLen = pAacEncoder->uEncBufSize;
                *puPcmBufUsed = 0;
                *puTimestampMs = pAacEncoder->uPcmTimestampMs;
            }
            else
            {
                /* The incoming PCM data size is not enough to do one AAC encoding, but we pull the PCM data into buffer here. */
                if (pAacEncoder->uPcmBufLen == 0)
                {
                    pAacEncoder->uPcmTimestampMs = getEpochTimestampInMs();
                }
                memcpy(pAacEncoder->pPcmBuf + pAacEncoder->uPcmBufLen, pPcmBuf, uPcmBufLen);
                pAacEncoder->uPcmBufLen += uPcmBufLen;
                *puEncBufLen = 0;
                *puPcmBufUsed = uPcmBufLen;
                *puTimestampMs = pAacEncoder->uPcmTimestampMs;
            }
        }
        else
        {
            uEncBufLen = pAacEncoder->uEncBufSize;
            if (uEncBufSize < uEncBufLen)
            {
                res = ERRNO_FAIL;
            }
            else if (uPcmBufLen + pAacEncoder->uPcmBufLen < pAacEncoder->uPcmBufSize)
            {
                res = ERRNO_FAIL;
            }
            else
            {
                /* TODO: Here we copy a silent AAC audio into the destinate encode buffer.
                 * You need to add your AAC encoder here to do the actual encoding.  */
                memcpy(pAacEncoder->pPcmBuf + pAacEncoder->uPcmBufLen, pPcmBuf, pAacEncoder->uPcmBufSize - pAacEncoder->uPcmBufLen);

                if (pAacEncoder->uChannels == 1)
                {
                    pSilentAacBuf = pSilentAac1Ch;
                    uSilentAacLen = AAC_OUTPUT_ENC_BUF_SIZE;
                }
                else
                {
                    pSilentAacBuf = pSilentAac2Ch;
                    uSilentAacLen = AAC_OUTPUT_ENC_BUF_SIZE * 2;
                }

                memcpy(pEncBuf, pSilentAacBuf, uSilentAacLen);
                uEncBufLen = uSilentAacLen;

                *puEncBufLen = uEncBufLen;
                *puPcmBufUsed = pAacEncoder->uPcmBufSize - pAacEncoder->uPcmBufLen;
                *puTimestampMs = pAacEncoder->uPcmTimestampMs;
                pAacEncoder->uPcmBufLen = 0;
            }
        }
    }

    return res;
}
