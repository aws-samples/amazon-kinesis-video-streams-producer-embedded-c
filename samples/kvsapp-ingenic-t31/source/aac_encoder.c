typedef struct AacEncoder
{
    unsigned int uSampleRate;
    unsigned int uChannels;
    unsigned int uBitRate;

    uint64_t uPcmTimestampMs;   /* The timestamp of first PCM sample in the buffer. */
    uint8_t *pPcmBuf;           /* The PCM buffer */
    size_t uPcmBufSize;         /* The PCM buffer size */
    size_t uPcmBufLen;          /* The data length in bytes in the PCM buffer */

    size_t uEncBufSize;         /* The max size of encoding buffer */
} AacEncoder_t;

static uint64_t getEpochTimestampInMs(void)
{
    uint64_t timestamp = 0;

    struct timeval tv;
    gettimeofday(&tv, NULL);
    timestamp = (uint64_t)(tv.tv_sec) * 1000 + (uint64_t)(tv.tv_usec) / 1000;

    return timestamp;
}

void *AacEncoder_create(const unsigned int uSampleRate, const unsigned int uChannels, const unsigned int uBitRate)
{
    int res = ERRNO_NONE;
    AacEncoder_t *pAacEncoder = NULL;

    if (uSampleRate == 0 || uChannels == 0 || uBitRate == 0)
    {
        printf("%s: Invalid parameters\n", __FUNCTION__);
        res = ERRNO_FAIL;
    }
    else if((pAacEncoder = (AacEncoder_t *)malloc(sizeof(AacEncoder_t))) == NULL)
    {
        printf("OOM: pAacEncoder\n");
        res = ERRNO_FAIL;
    }
    else
    {
        memset(pAacEncoder, 0, sizeof(AacEncoder_t));

        pAacEncoder->uSampleRate = uSampleRate;
        pAacEncoder->uChannels = uChannels;
        pAacEncoder->uBitRate = uBitRate;

        pAacEncoder->uPcmBufSize = AAC_INPUT_PCM_BUF_SIZE * uChannels;

        if ((pAacEncoder->pPcmBuf = (uint8_t *)malloc(pAacEncoder->uPcmBufSize)) == NULL)
        {
            printf("%s(): OOM: pPcmBuf\n", __FUNCTION__);
        }
        else
        {
            pAacEncoder->uPcmBufLen = 0;
        }

        pAacEncoder->uEncBufSize = AAC_OUTPUT_ENC_BUF_SIZE * uChannels;
    }

    if (res != ERRNO_NONE)
    {
        if (pAacEncoder != NULL)
        {
            if (pAacEncoder->pPcmBuf != NULL)
            {
                free(pAacEncoder->pPcmBuf);
            }
            free(pAacEncoder);
            pAacEncoder = NULL;
        }
    }

    return (void *)pAacEncoder;
}

void AacEncoder_terminate(void *pHandle)
{
    AacEncoder_t *pAacEncoder = (AacEncoder_t *)pHandle;

    if (pAacEncoder != NULL)
    {
        if (pAacEncoder->pPcmBuf != NULL)
        {
            free(pAacEncoder->pPcmBuf);
        }
        free(pAacEncoder);
    }
}

